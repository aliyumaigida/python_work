value1= int(input("Enter first value: "))
value2 = int(input("Enter second value: "))
operator = input("Enter an operator: ")


if operator == '+':
    print(value1 + value2)
elif operator == '-':
    print(value1 - value2)
elif operator == '*':
    print(value1 * value2)
elif operator == '**':
    print(value1 ** value2)    

elif operator == '/':
    print(value1 / value2)
elif operator == '%':
    print(value1 % value2)

